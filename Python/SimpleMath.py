# Addition Operator -> + 

print('3+5')                #output 1 -> 3+5
print('3'+'5')              #output 2 -> 35
print(3+5)                  #output 3 -> 8

#NOTE: Only texts should be placed in the quotes, but when we want to calculate arithmetic operations
# we shouldn't place in the quotes.

# Substraction Operator -> - 

print('3-5')                #output 1 -> 3-5
#print('3'-'5')              #output 2 -> error
print(3-5)                  #output 3 -> -2

# Multiplication Operator -> * 
# '5*7' or '5 multiplied by 7' or '5 times 7'
# '5 times 7' this is the way python reads multiplication


print('3*5')                #output 1 -> 3*5
#print('3'*'5')              #output 2 -> error
print('hello'*3)            #output 3 -> hellohellohello
print(3*'5')                #output 3 -> 555
print(3*5)                  #output 4 -> 15

